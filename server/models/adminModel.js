const mongoose = require("mongoose");
const { Schema } = mongoose;

const adminSchema = new Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
    unique: true
  }
});

module.exports = mongoose.model("Admin", adminSchema);
